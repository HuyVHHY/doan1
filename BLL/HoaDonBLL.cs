        public bool checkid(string id)
        {
            if (hoaDonDAO.Instance.kiemtraMa(id))
            {
                return true;
            }
            else { return false; }

        }

        public string them(string id,
            string idNv,
           string idkh,
           string ngayNhap)
        {
            if (checkid(id))
            {
                if (hoaDonDAO.Instance.them(id, idNv, idkh, ngayNhap))
                {
                    return "thành công";
                }
                else
                {
                    return "thất bại";
                }
            }
            else
            {
                return " id bị trùng";
            }
        }




        public string xoa(string id)
        {
            if (hoaDonDAO.Instance.xoa(id))
            {
                return "thành công";
            }
            else
            {
                return "thất bại";
            }

        }


